                    i) Para componentes significativos auditados por un auditor del componente, participar en el proceso de evaluación del riesgo del auditor del componente para identificar riesgos clave de error significativo de los estados financieros de grupo. El auditor de grupo debe:<br />
                    - Discutir con el auditor del componente o la gerencia del componente las actividades de negocio del componente que son significativas para el grupo;<br />
                    - Discutir con el auditor del componente acerca de la susceptibilidad del componente a un error en la importancia relativa de la información financiera por fraude o error;<br />
                    - Revisar la documentación del auditor del componente de los riesgos significativos identificados de error en la importancia relativa de los estados financieros de grupo.<br />
                    ii) Informar a los auditores del componente de los riesgos clave que pudieran tener un impacto en la auditoría; y<br />
